CREATE SEQUENCE SEQ_ESTUDIANTE START WITH 1 INCREMENT BY 1;
CREATE TABLE ESTUDIANTE (
    ID NUMBER DEFAULT SEQ_ESTUDIANTE.NEXTVAL PRIMARY KEY,
    CODIGO VARCHAR2(50),
    NOMBRE VARCHAR2(100),
    CARRERA_ID NUMBER
);
